-- Триггер 1: Только одна активная рецептура на продукт
GO
CREATE TRIGGER trg_единственная_активная_рецептура
ON рецептуры
AFTER INSERT, UPDATE
AS
BEGIN
    IF EXISTS (
        SELECT продукт_id, COUNT(*)
        FROM рецептуры
        WHERE статус = 'активен'
        GROUP BY продукт_id
        HAVING COUNT(*) > 1
    )
    BEGIN
        RAISERROR('Для одного продукта не может быть более одной активной рецептуры!', 16, 1);
        ROLLBACK;
    END
END
GO

-- Триггер 2: Только одна утвержденная ТК на продукт
CREATE TRIGGER trg_единственная_утвержденная_тк
ON технологические_карты
AFTER INSERT, UPDATE
AS
BEGIN
    IF EXISTS (
        SELECT продукт_id, COUNT(*)
        FROM технологические_карты
        WHERE статус = 'утверждена'
        GROUP BY продукт_id
        HAVING COUNT(*) > 1
    )
    BEGIN
        RAISERROR('Для одного продукта не может быть более одной утвержденной ТК!', 16, 1);
        ROLLBACK;
    END
END
GO

-- Триггер 3: Сумма компонентов = 100% при активации
CREATE TRIGGER trg_сумма_компонентов_100
ON рецептуры
AFTER UPDATE
AS
BEGIN
    DECLARE @сумма DECIMAL(10,2);
    DECLARE @рец_id INT;
    
    SELECT @рец_id = id FROM inserted WHERE статус = 'активен';
    
    IF @рец_id IS NOT NULL
    BEGIN
        SELECT @сумма = SUM(процент) FROM компоненты_рецептуры WHERE рецептура_id = @рец_id;
        
        IF @сумма != 100
        BEGIN
            RAISERROR('Сумма компонентов должна быть 100%%! Текущая сумма: %.2f%%', 16, 1, @сумма);
            ROLLBACK;
        END
    END
END
GO

